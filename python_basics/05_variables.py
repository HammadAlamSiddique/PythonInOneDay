# variables: objects containing specific values
x = 5       # numeric or integer variable
print(x)

y = "My love is dangerous"      # string variable
print(y)

x = x+10   # or x=15
print(x)

# type/class of variables  ---> type(variable)
print(type(x))
print(type(y))


# Rules to assign a variable:
#1. The variable should contain letters, numbers or underscores
#2. Do not start with numbers
#3. Spaces are not allowec
#4. Do not use keywords used in functions (break, mean, media, test, etc)
#5. The variable should be short and descriptive
#6. Case sensitivity (Lower case and upper case letters,  lower case letters should be used)

fruit_basket = "Bananas, Mangoes, Apples"
fruit_basket = 6
del fruit_basket
print(type(fruit_basket))
print(fruit_basket)